#ifndef FORMULAS_H_
#define FORMULAS_H_



#endif /* FORMULAS_H_ */

/** \brief suma dos numeros de tipo entero
 *
 * \param a float primer valor ingresado
 * \param b float segundo valor ingresado
 * \return float muestra la suma de los dos valores ingresados
 *
 */
int suma(int a, int b);

/** \brief resta dos numeros de tipo entero
 *
 * \param a float primer valor ingresado
 * \param b float segundo valor ingresado
 * \return float muestra la resta de los dos valores ingresados
 *
 */
int resta(int a, int b);

/** \brief divion entre dos numeros de tipo entero
 *
 * \param a float primer valor ingresado (dividendo)
 * \param b float segundo valor ingresado (divisor)
 * \return float muestra la division entre los dos valores ingresados
 *
 */
float division(int a, int b);

/** \brief multiplicacion entre dos numeros de tipo flotante
 *
 * \param a float primer valor ingresado
 * \param b float segundo valor ingresado
 * \return float muestra el producto entre los dos valores ingresados
 *
 */
int multiplicacion(int a, int b);

/** \brief calcula el factorial de un numero entero positivo
 *
 * \param a int valor ingresado para factorizar
 * \return int factorial del numero ingresado
 *
 */
int factorial(int a);
